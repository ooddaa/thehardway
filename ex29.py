people = 20
cats = 30
dogs = 15

if people < cats:
	print("Too many cats!")

if people > cats:
	print("Not many cats!")

if people < dogs:
	print("Dogs are plenty!")

if people > dogs:
	print("People need more dogs")

dogs += 5

if people >= dogs:
	print("People are greater or equal to dogs.")
	

if people <= dogs:
	print("People are less or equal to dogs.")

if people == dogs:
	print("It's ok, everyone is equal")


